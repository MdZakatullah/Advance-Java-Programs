package com.jsp.stringpgms;

public class ReplaceMethod {

	public static void main(String[] args) 
	{
		String s1 = "developer";
		
		System.out.println(s1.replace('e', '@'));
		System.out.println(s1.replace("lop", "LOP"));
		System.out.println(s1.replace("e", ""));
		System.out.println(s1.replace('d', 'D'));
	}
}
